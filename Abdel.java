
public class Abdel {
	boolean cringe;
	boolean unbased;
	boolean ballin;
	String name;
	int age;
	
	
	public Abdel() {
		cringe = true;
		unbased = true;
		ballin = false;
		name = "Abdel";
		age = 19;
	}


	public boolean isCringe() {
		return cringe;
	}


	public void setCringe(boolean cringe) {
		this.cringe = cringe;
	}


	public boolean isUnbased() {
		return unbased;
	}


	public void setUnbased(boolean unbased) {
		this.unbased = unbased;
	}


	public boolean isBallin() {
		return ballin;
	}


	public void setBallin(boolean ballin) {
		this.ballin = ballin;
	}


	public String getName() {
		return name;
	}


	public void setName(String name) {
		this.name = name;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}
	
	
}
